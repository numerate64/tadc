/* Shared navigation, particles, loading interstitial, and Konami-code easter egg. */
document.addEventListener("DOMContentLoaded", () => {
  const toggle = document.querySelector(".nav-toggle");
  const nav = document.querySelector(".site-nav");
  if (toggle && nav) {
    toggle.addEventListener("click", () => {
      const open = nav.classList.toggle("open");
      toggle.setAttribute("aria-expanded", String(open));
    });
  }

  const palette = ["#ff3cac", "#20e3ff", "#9d4edd", "#ffe66d"];
  const particleCount = window.matchMedia("(prefers-reduced-motion: reduce)").matches ? 0 : 14;
  for (let i = 0; i < particleCount; i += 1) {
    const particle = document.createElement("span");
    particle.className = "particle";
    const size = 4 + Math.random() * 10;
    Object.assign(particle.style, {
      left: `${Math.random() * 100}%`,
      width: `${size}px`,
      height: `${size}px`,
      background: palette[i % palette.length],
      animationDuration: `${12 + Math.random() * 18}s`,
      animationDelay: `${-Math.random() * 20}s`
    });
    document.body.appendChild(particle);
  }

  const overlay = document.createElement("div");
  overlay.className = "loading-overlay";
  overlay.innerHTML = '<div class="loading-box"><div class="loading-spinner"></div><h2>Hold onto your polygons!</h2><p id="loading-message"></p></div>';
  document.body.appendChild(overlay);

  document.querySelectorAll('a[data-transition]').forEach((link) => {
    link.addEventListener("click", (event) => {
      if (event.metaKey || event.ctrlKey || event.shiftKey) return;
      event.preventDefault();
      overlay.querySelector("#loading-message").textContent =
        CIRCUS_DATA.loadingMessages[Math.floor(Math.random() * CIRCUS_DATA.loadingMessages.length)];
      overlay.classList.add("show");
      setTimeout(() => { window.location.href = link.href; }, 360);
    });
  });

  const code = ["ArrowUp","ArrowUp","ArrowDown","ArrowDown","ArrowLeft","ArrowRight","ArrowLeft","ArrowRight","b","a"];
  let codeIndex = 0;
  window.addEventListener("keydown", (event) => {
    codeIndex = event.key === code[codeIndex] ? codeIndex + 1 : 0;
    if (codeIndex === code.length) {
      codeIndex = 0;
      document.body.classList.add("glitching");
      setTimeout(() => document.body.classList.remove("glitching"), 1000);
      window.CircusAchievements?.unlock("glitch_whisperer");
    }
  });
});
